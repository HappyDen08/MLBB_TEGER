"""Спільні налаштування опитування — використовуються і вебхуком, і щоденним скриптом."""

QUESTION = "Хто коли гратиме сьогодні?"

OPTIONS = [
    "12-14",
    "14-16",
    "16-18",
    "18-20",
    "20-22",
    "22-00",
    "+1 година з вибору",
    "-1 година з вибору",
    "зайнятий весь день",
    "вільний весь день, кличте",
    "Нічний час (затверджуйте самі)",
]


async def send_game_poll(bot, chat_id):
    """Надсилає опитування про гру у вказаний чат."""
    return await bot.send_poll(
        chat_id=chat_id,
        question=QUESTION,
        options=OPTIONS,
        is_anonymous=False,            # видно, хто за що проголосував
        allows_multiple_answers=True,  # можна обрати кілька варіантів
    )
